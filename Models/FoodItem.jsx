class FoodItem {
    constructor (name, calories, carbs, fat, protein, sugar, sodium, cholesterol, fiber) {
        this.name = name
        this.calories = calories
        this.carbs = carbs
        this.fat = fat
        this.protein = protein
        this.sugar = sugar
        this.sodium = sodium
        this.cholesterol = cholesterol
        this.fiber = fiber
    }


}